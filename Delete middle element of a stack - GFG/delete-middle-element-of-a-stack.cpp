class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        // code here.. 
        
        stack<int>temp;
        int  pos  =  sizeOfStack - (sizeOfStack+1)/2 ;
        
        for(int i  =0;i<pos;i++){
            int x  = s.top();
            s.pop();
            temp.push(x);
            
        }
        
        s.pop();
        
        for(int  i =0;i<pos;i++){
            s.push(temp.top());
            temp.pop();
        }
    }
};

